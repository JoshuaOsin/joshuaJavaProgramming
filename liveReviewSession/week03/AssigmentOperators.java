package week03;

public class AssigmentOperators {

    public static void main(String[] args) {

        int i = 100;

        System.out.println("i = " + i);

        i +=10;

        System.out.println("i = " + i);

        i*=3; // re-assign i values change each time
        System.out.println("i = " + i);

        i%=5;
        System.out.println("i = " + i);

        String statement = "EU8";

        statement +=" is Great";

        System.out.println("statement = " + statement);

    }


}
