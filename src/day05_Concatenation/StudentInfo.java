package day05_Concatenation;

public class StudentInfo {

    public static void main(String[] args) {

        String studentName = "Joshua";
        int age = 47;
        char gender = 'M';
        String schoolName = "NPS";
        String phoneNumber1 = "(987)-666-5555";
        String phoneNumber2 = "(123)-444-5555";
        boolean fullTime = false;
        double GPA = 3.5;
        // printing
        System.out.println("studentName = " + studentName);
        System.out.println("age = " + age);
        System.out.println("gender = " + gender);
        System.out.println("schoolName = " + schoolName);
        System.out.println("phoneNumber = " + phoneNumber1);
        System.out.println("phoneNumber2 = " + phoneNumber2);
        System.out.println("fullTime = " + fullTime);
        System.out.println("GPA = " + GPA);
    }

}
/*
    			1. student name (String)
    			2. age (int)
    			3. gender (char)
    			4. school name (String)
    			5. phone number (String) (###)-###-####
    			6. full time (boolean)
 */