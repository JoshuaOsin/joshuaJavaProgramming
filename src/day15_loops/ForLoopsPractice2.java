package day15_loops;

public class ForLoopsPractice2 {
    public static void main(String[] args) {
        for (char i = 'A'; i <='Z'; i++) {
            System.out.print( i+" ");
        }
        System.out.println("\n----------------------");
        for (char i = 'a'; i <='z'; i++) {
            System.out.print( i+" ");
        }
    }
}
