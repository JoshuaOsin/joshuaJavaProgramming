package day15_loops;

public class ForLoopIntro {

    public static void main(String[] args) {
        // int i = 1 -> initilization ; i<=10  -> condition; i++ -> iteration
        for (int i = 1; i<=10; i++) { // initilization and condition are mandatory to give
            System.out.println(i+ ". Hello World!");
        }

    }
}
