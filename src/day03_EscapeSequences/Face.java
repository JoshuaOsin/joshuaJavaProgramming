package day03_EscapeSequences;

public class Face {

    public static void main(String[] args) {

        System.out.println(" +\"\"\"\"\"+ ");
        System.out.println("[| o o |]");
        System.out.println(" |  ^  | ");
        System.out.println(" | '-' | ");
        System.out.println(" +-----+ ");


    }


}
/*
        You can insert several special characters into a string.
        The following list shows these special characters;
        note that each is preceded by a backslash (\).
        \" shows "
        \' shows '
        \\ show \
*/